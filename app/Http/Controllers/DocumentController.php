     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $url = $request->file('file')->store('public');
        $doc = Storage::url($url);
        $document = new Document();
        $document -> url = $doc;
        $document -> save();
        return redirect(route('documents.index'));
    }

